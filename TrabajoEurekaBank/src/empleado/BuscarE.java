        public void MostrarObjetos(ArrayList<Empleado> list,String codigo){
         Iterator<Empleado> iterador=list.iterator();
        DefaultTableModel model= new DefaultTableModel();
        String Col[]= {"Codigo", "Ap. Paterno", "Ap. Materno", "Nombre", "Ciudad", "Direccion", "Usuario", "Clave"};
        model.setColumnIdentifiers(Col);
        Object fila[] = new Object[Col.length];
        Empleado empleado=null;
        while(iterador.hasNext()){
            empleado =iterador.next();
            if(empleado.getCodigo().compareTo(codigo)==0){
            fila[0] = empleado.getCodigo();
            fila[1] = empleado.getPaterno();
            fila[2] = empleado.getMaterno();
            fila[3] = empleado.getNombre();
            fila[4] = empleado.getCiudad();
            fila[5] = empleado.getDireccion();
            fila[6] = empleado.getUsuario();
            fila[7] = empleado.getClave();
            model.addRow(fila);
            }
        }
        tblEmpleados.setModel(model);
    }
