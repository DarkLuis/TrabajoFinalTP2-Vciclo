    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String codigo = txtCodigoSucursal.getText();
        String nombre = txtNombre.getText();
        String ciudad = txtCiudad.getText();
        String direccion = txtDireccion.getText();
        int contadorCuenta = Integer.parseInt(txtNumeroSucursales.getText());
        Sucursal sucur= new Sucursal(codigo, nombre, ciudad, direccion, contadorCuenta);
        new SucursalBL().ModificarSucursal(sucur);
    }//GEN-LAST:event_btnModificarActionPerformed

