        public void MostrarObjetos(ArrayList<Cliente> list,String codigo){
         Iterator<Cliente> iterador=list.iterator();
        DefaultTableModel model= new DefaultTableModel();
         String Col[]= {"Codigo", "Ap. Paterno", "Ap. Materno", "Nombre", "DNI", "Ciudad", "Direccion", "Tel / Cel","E-mail"};
        model.setColumnIdentifiers(Col);
        Object fila[] = new Object[Col.length];
        Cliente cliente=null;
        while(iterador.hasNext()){
            cliente =iterador.next();
            if(cliente.getCodigo().compareTo(codigo)==0){
            fila[0] = cliente.getCodigo();
            fila[1] = cliente.getApellidoPaterno();
            fila[2] = cliente.getApellidoMaterno();
            fila[3] = cliente.getNombre();
            fila[4] = cliente.getDni();
            fila[5] = cliente.getCiudad();
            fila[6] = cliente.getDireccion();
            fila[7] = cliente.getTelefono();
            fila[8] = cliente.getEmail();
            model.addRow(fila);
            }
        }
        tblCliente.setModel(model);
    }
