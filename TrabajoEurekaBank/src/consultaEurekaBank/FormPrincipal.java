    private void btnAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirActionPerformed


        if(cboOpciones.getSelectedIndex()==0)
             showMessageDialog(null, "Elija Una Opcion", "Error", 0);
        if(cboOpciones.getSelectedIndex()==1){
          ConsultasEurekaBank cE= new ConsultasEurekaBank();
          cE.setVisible(true);
        }
       if(cboOpciones.getSelectedIndex()==2){
           code= JOptionPane.showInputDialog("Clave del Empleado :");
           if(new EmpleadoBL().buscarEmpleado(code)!=-1){
                ConsultaEmpleado cs = new ConsultaEmpleado();
                Empl=false;//crear una funcion q inavilite las opciones de la tabla conuslta empleado
                cs.InavilitarOpciones(Empl,code);
                cs.setVisible(true);
           }else
               showMessageDialog(null, "Clave Incorrecta", "Error", 0);
       }
       if(cboOpciones.getSelectedIndex()==3){
           code= JOptionPane.showInputDialog("Clave del Cliente :");
           if(new ClienteBL().buscarCliente(code)!=null){
                ConsultaClientes cc = new ConsultaClientes();
                  Empl=false;
                  cc.InavilitarOpciones(Empl,code);
                cc.setVisible(true);
           }else
               showMessageDialog(null, "Clave Incorrecta", "Error", 0);
       }
        btnAbrir.setEnabled(false);
        cboOpciones.setFocusable(false);
        
    }//GEN-LAST:event_btnAbrirActionPerformed

