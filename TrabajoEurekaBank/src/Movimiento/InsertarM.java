    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        String param1= txtmov1.getText();
        int param2= Integer.parseInt(txtmov2.getText());
        int dia= Integer.parseInt( String.valueOf(cboDia.getSelectedItem()));
        int mes= Integer.parseInt(String.valueOf( cboMes.getSelectedItem()));
        int anyo= Integer.parseInt(String.valueOf(cboAnio.getSelectedItem()));
        Fecha fecha= new Fecha(dia,mes,anyo);
        String param4= txtmov4.getText();
        String param5= txtmov5.getText();
        float param6= Float.valueOf(txtmov6.getText());
        String param7= txtmov7.getText();
         if(fecha.VerificarFecha()){
            Movimiento movimiento= new Movimiento(param1,param2,fecha,param4,param5,param6,param7);
            new MovimientoBL().insertarMovimiento(param1, param2, fecha, param4, param5, param6, param7);
        } else
             showMessageDialog(null, "Error al fecha Incorrecta", "Error", 0);
        Activar(false);


    }//GEN-LAST:event_btnInsertarActionPerformed

