    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        modelo = new DefaultTableModel();
        ArrayList<Contador> contadores = new ArrayList<Contador>();
        contadores = new ContadorBL().listarContador();
        String columnas[] = {"Tabla", "Item","Longitud"};
        Iterator<Contador> iterador = contadores.iterator();
        modelo.setColumnIdentifiers(columnas);
        Object fila[] = new Object[columnas.length];
        Contador contador = null;
        while(iterador.hasNext()) {
            contador = iterador.next();
            fila[0] = contador.getTabla();
            fila[1] = contador.getItem();
            fila[2] = contador.getLongitud();
            modelo.addRow(fila);
        }
        tblContador.setModel(modelo);
}//GEN-LAST:event_btnListarActionPerformed

