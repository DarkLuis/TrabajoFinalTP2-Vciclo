    private void btnListarCostoMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarCostoMActionPerformed
        // TODO add your handling code here:
        modelo = new DefaultTableModel();
        String columnas[] = {"Código de moneda","Importe"};
        Object fila[] = new Object[columnas.length];
        ArrayList<CostoMovimiento> lista = new ArrayList<CostoMovimiento>();
        lista = new CostoMovimientoBL().listarCostoMovimiento();
        modelo.setColumnIdentifiers(columnas);
        Iterator<CostoMovimiento> iterador = lista.iterator();
        CostoMovimiento costoMovimiento=null;
        while(iterador.hasNext()) {
            costoMovimiento = iterador.next();
            fila[0] = costoMovimiento.getMonecodigo();
            fila[1] = costoMovimiento.getCostimporte();
            modelo.addRow(fila);
        }
        tblCostMov.setModel(modelo);
}//GEN-LAST:event_btnListarCostoMActionPerformed

