private void BtnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnInsertarActionPerformed
// TODO add your handling code here:
    String sucucodigo,asigcodigo, emplcodigo ;
        int rpta, dia=0, mes=0, año=0, dia1=0, mes1=0, año1=0;
        GregorianCalendar fechaAlta, fechaBaja;
        asigcodigo = TxtasigCod.getText();
        sucucodigo = TxtsucuCod.getText();
        emplcodigo = TxtemplCod.getText();
        dia=(Integer.parseInt(TxtDiaA.getText()));
        mes=(Integer.parseInt(TxtMesA.getText()));
        año=(Integer.parseInt(TxtAñoA.getText()));
        fechaAlta = new GregorianCalendar(año,(mes-1),dia);
        dia1=(Integer.parseInt(TxtDiaB.getText()));
        mes1=(Integer.parseInt(TxtMesB.getText()));
        año1=(Integer.parseInt(TxtAñoB.getText()));
        fechaBaja= new GregorianCalendar(año1, (mes1-1), dia1);
        if(sucucodigo != null) {
            rpta = new AsignadoBL().insertarAsignado(asigcodigo, sucucodigo, emplcodigo, fechaAlta, fechaBaja);
            switch(rpta) {
                case 0:
                case 1:Nuevo();
                    activar(false);
                    break;
                case 2:
                    TxtasigCod.setText(null);
                    TxtasigCod.requestFocus();
                    break;
                case 3:
                    Nuevo();
                    TxtasigCod.requestFocus();
                    break;

            }
        } else
            showMessageDialog(this, "Debe digitar datos", "Aviso", 2);

}//GEN-LAST:event_BtnInsertarActionPerformed

