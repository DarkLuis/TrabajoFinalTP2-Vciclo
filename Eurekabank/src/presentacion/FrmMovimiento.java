    private void BtnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnInsertarActionPerformed
        // TODO add your handling code here:
        String cuencodigo, emplcodigo, tipocodigo, cuenreferencia ;
        int numero, rpta, dia, mes, año;
        double importe;
        GregorianCalendar fecha;
        cuencodigo = txtCodigo.getText();
        numero = Integer.parseInt(txtNumero.getText());
        dia=(Integer.parseInt(txtDia.getText()));
        mes=(Integer.parseInt(txtMes.getText()));
        año=(Integer.parseInt(txtAño.getText()));
        fecha = new GregorianCalendar(año,(mes-1),dia);
        emplcodigo = txtCEmpleado.getText();
        tipocodigo = txtTipoCodigo.getText();
        importe = Double.parseDouble(txtImporte.getText());
        cuenreferencia = txtReferencia.getText();

        if(cuencodigo != null) {
            rpta = new MovimientoBL().insertarMovimiento(cuencodigo, numero, fecha, emplcodigo, tipocodigo, importe, cuenreferencia);
            switch(rpta) {
                case 0:
                case 1:
                    limpiarTextos();
                    activar(false);
                    break;
                case 2:
                    txtCodigo.setText(null);
                    txtCodigo.requestFocus();
                    break;
                case 3:
                    limpiarTextos();
                    txtCodigo.requestFocus();
                    break;
            }
        } else
            showMessageDialog(this, "Debe digitar datos", "Aviso", 2);
    }//GEN-LAST:event_BtnInsertarActionPerformed

    private void limpiarTextos() {
        txtCodigo.setText(null);
        txtNumero.setText(null);
        txtDia.setText(null);
        txtMes.setText(null);
        txtAño.setText(null);
        txtTipoCodigo.setText(null);
        txtCEmpleado.setText(null);
        txtImporte.setText(null);
        txtReferencia.setText(null);
    }

    private void activar(boolean estado) {
        txtCodigo.setEnabled(estado);
        txtNumero.setEnabled(estado);
        txtDia.setEnabled(estado);
        txtMes.setEnabled(estado);
        txtAño.setEnabled(estado);
        txtTipoCodigo.setEnabled(estado);
        txtCEmpleado.setEnabled(estado);
        txtImporte.setEnabled(estado);
        txtReferencia.setEnabled(estado);
        BtnInsertar.setEnabled(estado);
        BtnNuevo.setEnabled(!estado);
    }
