private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        String tabla;
        int rpta,item,longitud;
        tabla = txtTabla.getText();
        item = (Integer.parseInt(txtItem.getText()));
        longitud = (Integer.parseInt(txtLongitud.getText()));
        if( tabla!= null && item!=0 && longitud!= 0) {
            rpta = new ContadorBL().insertarContador(tabla,item,longitud);
            switch(rpta) {
                case 0:
                case 1:
                    limpiarTextos();
                    activar(false);
                    break;
                case 2:
                    txtTabla.setText(null);
                    txtTabla.requestFocus();
                    break;
                case 3:
                    limpiarTextos();
                    txtTabla.requestFocus();
                    break;
            }
        } else
            showMessageDialog(this,"Debe digitar datos","Aviso",2);
}//GEN-LAST:event_btnInsertarActionPerformed

