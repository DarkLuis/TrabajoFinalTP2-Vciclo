    private void limpiarTextos() {
        txtTabla.setText(null);
        txtItem.setText(null);
        txtLongitud.setText(null);
    }
        private void activar(boolean estado) {
        txtTabla.setEnabled(estado);
        txtItem.setEnabled(estado);
        txtLongitud.setEnabled(estado);
        btnInsertar.setEnabled(estado);
        btnNuevo.setEnabled(!estado);
    }
