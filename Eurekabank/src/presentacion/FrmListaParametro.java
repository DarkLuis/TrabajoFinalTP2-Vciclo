    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        modelo = new DefaultTableModel();
        ArrayList<Parametro> lista = new ArrayList<Parametro>();
        lista = new ParametroBL().listarParametro();
        String columnas[] = {"Código", "Descripción", "Valor", "Estado"};
        Iterator<Parametro> iterador = lista.iterator();
        modelo.setColumnIdentifiers(columnas);
        Object fila[] = new Object[columnas.length];
        Parametro parametro = null;
        while(iterador.hasNext()) {
            parametro= iterador.next();
            fila[0] = parametro.getCodigo();
            fila[1] = parametro.getDescripcion();
            fila[2] = parametro.getValor();
            fila[3] = parametro.getEstado();
            modelo.addRow(fila);
        }
        tblParametro.setModel(modelo);
}//GEN-LAST:event_btnListarActionPerformed

