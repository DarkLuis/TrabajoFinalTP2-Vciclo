    private void btnListarMonedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarMonedaActionPerformed
        modelo = new DefaultTableModel();
        String columnas[] = {"Código", "Descripción"};
        Object fila[] = new Object[columnas.length];
        ArrayList<Moneda> lista = new ArrayList<Moneda>();
        lista = new MonedaBL().listarMoneda();
        modelo.setColumnIdentifiers(columnas);
        Iterator<Moneda> iterador = lista.iterator();
        Moneda moneda=null;
        while(iterador.hasNext()) {
            moneda = iterador.next();
            fila[0] = moneda.getCodigo();
            fila[1] = moneda.getDescripcion();
            modelo.addRow(fila);
        }
        tblMonedas.setModel(modelo);
}//GEN-LAST:event_btnListarMonedaActionPerformed

