private void BtnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnInsertarActionPerformed
// TODO add your handling code here:
    String monecodigo;
    double costimporte;
        int rpta;
        monecodigo = TxtCod.getText();
        costimporte = Double.parseDouble(TxtImporte.getText());
        if(monecodigo != null && costimporte != 0 ) {
            rpta = new CostoMovimientoBL().insertarCostoMovimiento(monecodigo, costimporte);
            switch(rpta) {
                case 0:
                case 1:Nuevo();
                    activar(false);
                    break;
                case 2:
                    TxtCod.setText(null);
                    TxtCod.requestFocus();
                    break;
                case 3:
                    Nuevo();
                    TxtCod.requestFocus();
                    break;
            }
        } else
            showMessageDialog(this, "Todos los campos son obligatorios", "Aviso", 2);
}//GEN-LAST:event_BtnInsertarActionPerformed

